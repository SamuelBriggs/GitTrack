@Configuration


public class config {


    @Bean
    public SecurityFilterChain filterChain(HttpSecurity httpSecurity) throws Exception {


        AuthenticationSuccessHandler authenticationSuccessHandler = new SuccessHandler();
        httpSecurity.formLogin(Customizer.withDefaults()).csrf(AbstractHttpConfigurer::disable).
                cors(Customizer.withDefaults()).
                sessionManagement(c -> c.sessionCreationPolicy(SessionCreationPolicy.STATELESS))
                .oauth2Login(c -> c.defaultSuccessUrl("http://localhost:3000").successHandler(authenticationSuccessHandler)).authorizeHttpRequests(c -> c.requestMatchers("/help","/loginSuccess")
                .authenticated()).authorizeHttpRequests(c -> c.requestMatchers("/oauth").permitAll())
                ;
        return httpSecurity.build();
    }


    @Bean
    public WebMvcConfigurer corsConfigurer(){
        return new WebMvcConfigurer() {
            @Override
            public void addCorsMappings(CorsRegistry registry) {
                registry.addMapping("/**")
                        .allowedHeaders("*")
                        .allowedOrigins("*");
            }
        };
    }



}



